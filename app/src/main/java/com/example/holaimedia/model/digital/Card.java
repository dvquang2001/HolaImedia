package com.example.holaimedia.model.digital;

public class Card {
    private String name;
    private int money;

    public Card() {
    }

    public Card(String name, int money) {
        this.name = name;
        this.money = money;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }
}
